def generate_wish() -> str:
    """Пожелание дня с подколом."""
    today = date.today()
    weekday = WEEKDAYS[today.weekday()]

    if random.random() < 0.55:
        body = random.choice(CURATED)
    else:
        body = random.choice(TEMPLATES).format(
            weekday=weekday,
            thing=random.choice(THINGS),
            n=random.randint(1, 3),
            n2=random.randint(0, 2),
        )

    if random.random() < 0.3:
        return body

    return f"{random.choice(INTROS)}\n\n{body}"


def generate_compliment(name: Optional[str] = None) -> str:
    """Подкол в адрес пользователя."""
    if name and random.random() < 0.5:
        body = random.choice(COMPLIMENT_TEMPLATES).format(name=name)
    else:
        body = random.choice(COMPLIMENT_CURATED)

    if random.random() < 0.25:
        return body

    return f"{random.choice(COMPLIMENT_INTROS)}\n\n{body}"


def generate_genuine_compliment(name: Optional[str] = None) -> str:
    """Без подкола — для исключённого пользователя."""
    body = random.choice(GENUINE_COMPLIMENTS)
    if name:
        return f"Для {name}:\n\n{body}"
    return body
