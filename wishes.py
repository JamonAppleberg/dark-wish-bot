def generate_wish() -> str:
    """Случайное пожелание: готовое или из шаблона."""
    today = date.today()
    weekday = WEEKDAYS[today.weekday()]

    if random.random() < 0.55:
        body = random.choice(CURATED)
    else:
        template = random.choice(TEMPLATES)
        body = template.format(
            weekday=weekday,
            thing=random.choice(THINGS),
            n=random.randint(1, 3),
            n2=random.randint(0, 2),
        )

    if random.random() < 0.25:
        return body

    intro = random.choice(INTROS)
    return f"{intro}\n\n{body}"


def generate_compliment(name: Optional[str] = None) -> str:
    """Саркастичный комплимент, опционально с именем."""
    display_name = name or "ты"

    if random.random() < 0.55:
        body = random.choice(COMPLIMENT_CURATED)
    else:
        body = random.choice(COMPLIMENT_TEMPLATES).format(
            name=display_name,
            hour=random.choice([23, 0, 1, 2, 9, 10]),
        )

    if random.random() < 0.2:
        return body

    intro = random.choice(COMPLIMENT_INTROS)
    return f"{intro}\n\n{body}"


def generate_genuine_compliment(name: Optional[str] = None) -> str:
    """Настоящий комплимент для исключённого пользователя."""
    body = random.choice(GENUINE_COMPLIMENTS)
    if name:
        return f"💛 Для {name}:\n\n{body}"
    return f"💛 {body}"
